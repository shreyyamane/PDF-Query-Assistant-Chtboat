def search_and_answer(question,top_k=2):
  #query embedding

  qres = co.embed(
      model="embed-multilingual-v3.0",
      input_type="search_query",
      embedding_types=["float"],
      texts=[question]
  )
  query_emb = qres.embeddings.float[0]

  #pass to chroma tb

  res = collection.query(
      query_embeddings=[query_emb],
      n_results=top_k*2
  )

  top_text,top_images=[],[]

  for doc,meta in zip(res["documents"][0],res["metadatas"][0]):
    if meta["source"] == "txt":
      top_text.append(doc)
    else:
      top_images.append(doc)

  #only take relevant info

  top_texts = top_text[:top_k]
  top_images = top_images[:top_k]


  #prepare context

  context = "\n\n".join(top_texts + [f"[IMAGE: {img}]" for img in top_images])

  response_text = chain.invoke({
      "context": context,
      "question": question
  })

  return response_text,top_images

