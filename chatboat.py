max_pixel = 1568 * 1568

#resize

def resize_image(pil_image):
  og_w,og_h = pil_image.size
  if og_w * og_h > max_pixel:
    scale_factor = (max_pixel / (og_w * og_h))*0.5
    new_w = int(og_w * scale_factor)
    new_h = int(og_h * scale_factor)
    pil_image.thumbail((new_w, new_h)) #main aspect ratio


#base 64-def base64_from_image(img_path):


def base64_from_image(img_path):
  pil_image = Image.open(img_path).convert("RGB")
  resize_image(pil_image)

  with io.BytesIO() as image_file:
    pil_image.save(image_file, format="PNG")
    image_file.seek(0)
    return base64.b64encode(image_file.read()).decode("utf-8")

